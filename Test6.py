#------------------------
# type() بتظهر نوع البيانات اللى بتعامل معاها
# All data in python is object
#------------------------

print(10) # 10

print(type(10)) # int ممكن يكون سالب عادى

print(type(100.26)) # float => Floating point Number ممكن يكون سالب عادى

print(type("Hello Python")) # str => string نصوص وكدا

print(type([1, 2, 3, 4, 5])) # List => list

print(type((1, 2, 3, 4, 5))) # tuple => Tuple 

print(type({"One": 1, "Two": 2, "Three": 3})) # dict => Dictionary

print(type(2 == 2)) # bool => Boolean