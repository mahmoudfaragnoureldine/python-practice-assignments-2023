# Escape sequnce characters
# \b => Back Space
print("Hello\bWorld") # will remove o

# \newLine => Ecsape new line + \
print("Hello \
I love \
python")

# \\ => Escape Back Slash
print("I love \\")

# \' => Escape Single Quate
print('I love single quate \'Test\'  ')

# \" => Escape Double Quate
print("I love single quate \"Test\" ")

# \n => Line Feed
print("Hello World\nSecond Line") 

# \r => Carriage Retuen
print("123456\rAbcd")

# \t => Horizantal tab
print("Hello\tWorld")

# \xhh => Character hex value
print("\x4F") 